class UsersController <ApplicationController
  def index
    @users = User.all
  end

  def show
    @user = User.find(params[:id])
  end

  def new
    @user = User.new
  end

  def create
    @user = User.new(params[:user])
    #TMembership.find_or_create_by(@user.id)
    if @user.save
      redirect_to user_url(@user.id)
    else
      render :new
    end
  end

  def edit
